static int mRF_BlockType2CliffIndex(u8 type) {
    static int cliff_info[7] = {
        // clang-format off
        mRF_CLIFF_HORIZONTAL,
        mRF_CLIFF_BOT_RIGHT,
        mRF_CLIFF_VERTICAL_RIGHT,
        mRF_CLIFF_TOP_RIGHT,
        mRF_CLIFF_TOP_LEFT,
        mRF_CLIFF_VERTICAL_LEFT,
        mRF_CLIFF_BOT_LEFT,
        // clang-format on
    };

    int block_info = mRF_GetSystemBlockInfo(type);
    int i;

    for (i = 0; i < 7; i++) {
        if ((block_info & cliff_info[i]) != 0) {
            return i;
        }
    }

    return -1;
}

static int mRF_GetRandom(int max) {
#ifdef TARGET_PC
    u32 seed_before = qrand_get_seed();
    int result = RANDOM(max);
    s_rng_call_count++;
    if (s_tglog) {
        fprintf(s_tglog, "  RANDOM #%d: max=%d result=%d (seed=0x%08X -> 0x%08X)\n",
                s_rng_call_count, max, result, seed_before, qrand_get_seed());
        fflush(s_tglog);
    }
    return result;
#else
    return RANDOM(max);
#endif
}

static int mRF_D2ToD1(int bx, int bz) {
    return bz * BLOCK_X_NUM + bx;
}

static void mRF_Direct2BlockNo(int* bx, int* bz, int base_bx, int base_bz, int direct) {
    static int x_offset[mRF_DIRECT_NUM] = { 0, -1, 0, 1 };
    static int z_offset[mRF_DIRECT_NUM] = { -1, 0, 1, 0 };

    int idx = direct & 3;

    bx[0] = base_bx + x_offset[idx];
    bz[0] = base_bz + z_offset[idx];
}

static int mRF_CheckCorrectBlockNo(int bx, int bz, int bx_min, int bx_max, int bz_min, int bz_max) {
    if (bx < bx_min) {
        return FALSE;
    }

    if (bx > bx_max) {
        return FALSE;
    }

    if (bz < bz_min) {
        return FALSE;
    }

    if (bz > bz_max) {
        return FALSE;
    }

    return TRUE;
}

